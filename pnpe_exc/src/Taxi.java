public class Taxi {
    private String colour;
    private int year;

    public Taxi(String colour, int year) {
        this.colour = colour;
        this.year = year;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
